<script>
    const idInput = document.getElementById('id_number');
    const lastInput = document.getElementById('last_name');
    const userInput = document.getElementById('username');

    function generateUsername() {
        let lastName = lastInput.value.trim().toUpperCase();
        let idNumber = idInput.value.trim();
        
        // Get first 4 letters of last name
        let lastPart = lastName.substring(0, 4);
        
        if (lastPart && idNumber) {
            userInput.value = lastPart + idNumber;
        } else {
            userInput.value = '';
        }
    }

    // Listen for typing in either field
    idInput.addEventListener('input', generateUsername);
    lastInput.addEventListener('input', generateUsername);


    const passwordInput = document.getElementById('password');
const togglePassword = document.getElementById('togglePassword');

togglePassword.addEventListener('click', function () {
    // Toggle the type attribute
    const type = passwordInput.getAttribute('type') === 'password' ? 'text' : 'password';
    passwordInput.setAttribute('type', type);
    
    // Toggle the eye icon (optional: change icon look) 
    this.textContent = type === 'password' ? '👁️' : '🙈';
});
