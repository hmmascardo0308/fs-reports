// Check if form is submitted
if (isset($_POST['register'])) {
    $id_number   = trim($_POST['id_number']);
    
    // Convert names to UPPERCASE
    $first_name  = strtoupper(trim($_POST['first_name']));
    $middle_name = strtoupper(trim($_POST['middle_name']));
    $last_name   = strtoupper(trim($_POST['last_name']));
    
    $full_name   = trim($first_name . ' ' . ($middle_name ? $middle_name . ' ' : '') . $last_name);
    $username    = trim($_POST['username']); // Generated by JS, but captured here
    $user_type   = trim($_POST['user_type']);
    $password_plain = trim($_POST['password']);
    $password = password_hash($password_plain, PASSWORD_DEFAULT);
    $created_at  = date('Y-m-d H:i:s');

    // Basic validation
    $errors = [];
    if (!$id_number) $errors[] = "ID number is required.";
    if (!$first_name || !$last_name) $errors[] = "First and last name are required.";
    if (!$username) $errors[] = "Username is required.";
    if (!$password_plain) $errors[] = "Password is required.";
    if (!$user_type) $errors[] = "User type is required.";

    // Check if username already exists
    $stmt = $conn->prepare("SELECT id FROM fs_reports.qcl_user WHERE username = ?");
    $stmt->bind_param("s", $username);
    $stmt->execute();
    $stmt->store_result();
    if ($stmt->num_rows > 0) $errors[] = "Username already exists.";
    $stmt->close();

    if (empty($errors)) {
        $stmt = $conn->prepare("
            INSERT INTO fs_reports.qcl_user 
            (id_number, first_name, middle_name, last_name, full_name, username, user_type, password, created_at) 
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
        ");
        $stmt->bind_param(
            "sssssssss",
            $id_number, $first_name, $middle_name, $last_name, $full_name, $username, $user_type, $password, $created_at
        );

        if ($stmt->execute()) {
            $_SESSION['success'] = "User registered successfully!";
            header("Location: login.php");
            exit;
        } else {
            $errors[] = "Database error: " . $stmt->error;
        }
        $stmt->close();
    }
}
